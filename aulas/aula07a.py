# OS OPERADORES ARITIMÉTICOS SÂO:
# + é adição            ** é potenciação(elevado a)(tambem pode ser usado a função pow() por exemplo pow(4, 2)vai dar 64, com essa função vc perde a ordem de precedência)
# - é subtração         // é divisão inteira(é o resultado de dividir mas sem passar a virgula)
# * é multiplicação     %  é resto da divisão(é o resto que sobra de dividir sem passar a virgula)
# / é divisão

# ORDEM DE PRECEDÊNCIA(A ORDEM QUE O COMPUTADOR EXECUTA AS CONTAS):
# caso haja mais de um de uma fileira por exemplo 3*2/4(todos são da 3 fileira) ele fara o que aparecer primeiro
# nesse caso a * primeiro e depois a /
# 1-()
# 2-**
# 3-* / // %
# 4-+ -

#Você também pode multiplicar strings dessa forma:
print('Oi' * 5)

#Com o + voçê junta strings(o nome correto não é juntar e sim concatenar):
print('Oi' + 'Amigo!')

print(3 * 5 + 4 ** 2)